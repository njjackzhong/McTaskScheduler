public class MCXmlPreset {
    private String suffix;
    private String presetXml;
    private int index;

    public String getSuffix() {
        return suffix;
    }

    public void setSuffix(String suffix) {
        this.suffix = suffix;
    }

    public String getPresetXml() {
        return presetXml;
    }

    public void setPresetXml(String presetXml) {
        this.presetXml = presetXml;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
